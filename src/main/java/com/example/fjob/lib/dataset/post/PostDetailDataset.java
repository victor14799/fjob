package com.example.fjob.lib.dataset.post;

import com.example.fjob.common.CommonUtils;

import java.util.Date;
import java.util.List;

public class PostDetailDataset {

    private String postId;

    private String username;

    private String fullName;

    private String title;

    private String budget;

    private String content;

    private String tag;

    private String status;

    private Date startDate;

    private Date endDate;

    private Date insDate;

    private Date updDate;

    private String imgUrl;

    private List<String> skillLists;

    public List<String> getSkillLists() {
        return skillLists;
    }

    public void setSkillLists(List<String> skillLists) {
        this.skillLists = skillLists;
    }

    public String getImgUrl() {
        return imgUrl;
    }

    public void setImgUrl(String imgUrl) {
        this.imgUrl = imgUrl;
    }

    public String getPostId() {
        return postId;
    }

    public void setPostId(String postId) {
        this.postId = postId;
    }

    public String getFullName() {
        return fullName;
    }

    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getBudget() {
        return CommonUtils.formatMoney(this.budget);
    }

    public void setBudget(String budget) {
        this.budget = budget;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getTag() {
        return tag;
    }

    public void setTag(String tag) {
        this.tag = tag;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public Date getStartDate() {
        return startDate;
    }

    public void setStartDate(Date startDate) {
        this.startDate = startDate;
    }

    public Date getEndDate() {
        return endDate;
    }

    public void setEndDate(Date endDate) {
        this.endDate = endDate;
    }

    public Date getInsDate() {
        return insDate;
    }

    public void setInsDate(Date insDate) {
        this.insDate = insDate;
    }

    public Date getUpdDate() {
        return updDate;
    }

    public void setUpdDate(Date updDate) {
        this.updDate = updDate;
    }
}
